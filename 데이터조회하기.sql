--데이터 조회하기--
SELECT * FROM KOPO_PRODUCT_VOLUME
WHERE 1=1
AND PRODUCTGROUP IN ('ST0001', 'ST0002')
AND YEARWEEK BETWEEN 201501 AND 201530 --BETWEEN
AND YEARWEEK LIKE '%2015%' --LIKE
AND YEARWEEK IS NOT NULL; --NULL값이 아닌 값


-- A01 지역의 201724~25 201727~29
-- AP1NAME = BEST BUY, HOME DEPOT
-- PRODUCTGROUP = HHP

-- 연산자 사용하여 조회하기
SELECT * FROM KOPO_CHANNEL_RESULT
WHERE 1=1
AND (YEARWEEK BETWEEN 201724 AND 201725
OR YEARWEEK BETWEEN 201727 AND 201729) --OR조건 잘 숙지하기
AND AP1NAME IN ('BEST BUY', 'HOME DEPOT')
AND PRODUCT LIKE '%HHP%'
AND AP2ID IN ('A01');

