-- 정렬하기
SELECT
    REGIONID,
    PRODUCTGROUP,
    YEARWEEK
FROM KOPO_PRODUCT_VOLUME
WHERE PRODUCTGROUP = 'ST0001'
AND YEARWEEK BETWEEN 201501 AND 201503
ORDER BY YEARWEEK DESC, REGIONID ASC; --DESC : 내림차순, ASC : 오름차순

